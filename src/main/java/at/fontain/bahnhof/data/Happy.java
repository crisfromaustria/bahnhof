package at.fontain.bahnhof.data;

public class Happy {
	private String hostAddress;
	private String hostName;
	private String bahnhofName;
	private String bahnhofList;
	private String hostList;

	// getter & setter
	public String getHostAddress() {
		return hostAddress;
	}

	public void setHostAddress(String hostAddress) {
		this.hostAddress = hostAddress;
	}

	public String getHostName() {
		return hostName;
	}

	public void setHostName(String hostName) {
		this.hostName = hostName;
	}

	public String getBahnhofName() {
		return bahnhofName;
	}

	public void setBahnhofName(String bahnhofName) {
		this.bahnhofName = bahnhofName;
	}

	public String getBahnhofList() {
		return bahnhofList;
	}

	public void setBahnhofList(String bahnhofList) {
		this.bahnhofList = bahnhofList;
	}

	public String getHostList() {
		return hostList;
	}

	public void setHostList(String hostList) {
		this.hostList = hostList;
	}
}
